void _main() {
    ll N, K;
    cin >> N >> K;

    vector<int> A(N);
    REP(i, N) {
        cin >> A[i];
        --A[i];
    }

    vector<int> history(N, -1);
    history[0] = 0;
    int k = 0;
    int cnt = 0;
    REP(i, K) {
        DEBUG(i, k);
        k = A[k];
        ++cnt;
        if (history[k] != -1) break;
        history[k] = cnt;
    }

    if (history[k] < K) {
        K = history[k] + ((K - history[k]) % (cnt - history[k]));
    }

    DEBUG(history);
    DEBUG(cnt, k, K, history[k]);

    int ans = 0;
    REP(i, K) {
        ans = A[ans];
    }
    osf << ans + 1 << endl;

}

